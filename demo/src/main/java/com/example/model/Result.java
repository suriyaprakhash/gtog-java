package com.example.model;

/**
 * 
 * This is the object to store the question and the corresponding answer which
 * has been processed
 * 
 * @version 1.0
 * @since 2018-02-14
 *
 */
public class Result {
	String quetion;
	String answer;

	public String getQuetion() {
		return quetion;
	}

	public void setQuetion(String quetion) {
		this.quetion = quetion;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

}
